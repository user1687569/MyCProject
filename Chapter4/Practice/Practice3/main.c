#include <stdio.h>
/*
 * C89
 * 8  / 5  = 1
 * -8 / 5  = -1
 * 8  / -5 = -1
 * -8 / -5 = 1
**/

/*
 * C99
 * 8  / 5  = 1
 * -8 / 5  = -1
 * 8  / -5 = -1
 * -8 / -5 = 1
**/

int main()
{
    printf("8 / 5 = %d\n", 8 / 5);          /* 1  */
    printf("-8 / 5 = %d\n", -8 / 5);        /* -1 */
    printf("8 / -5 = %d\n", 8 / -5);        /* -1 */
    printf("-8 / -5 = %d\n", -8 / -5);      /* 1  */
    return 0;
}


